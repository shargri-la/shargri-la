// Custom types
/// u64.
pub type Slot = u64;
/// u64.
pub type Epoch = u64;
/// usize.
pub type Address = usize;

/// u128.
pub type Wei = u128;
/// u128.
pub type Gas = u128;
/// u128. wei/gas.
pub type GasPrice = Wei;

/// Account nonce. u128.
pub type Nonce = usize;
/// u64.
pub type TransactionHash = u64;
/// Data in a receipt. String.
pub type Data = String;
